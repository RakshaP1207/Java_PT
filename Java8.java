public class Java8 {
    public static void main(String[] args){
        int x=7;
        System.out.println(x);
        System.out.println(++x);
        System.out.println(x);
        
        int i=8;
        System.out.println(i);
        System.out.println(i++);
        System.out.println(i);
        System.out.println(++i);
        System.out.println(--i);
        System.out.println(--i);
        System.out.println(i++);
        System.out.println(i);
        
        
        
    }
    
}
